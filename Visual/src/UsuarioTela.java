    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed
        int i = JOptionPane.showConfirmDialog(rootPane, "Deseja mesmo excluir?");
        if(i == 0){
            try {
                arquivo = new ArquivoIndexado<>(Usuario.class, "usuario.db", "usuario1.idx", "usuario2.idx");
                arquivo.excluir(u.getCodigo());
                JOptionPane.showMessageDialog(rootPane, "Usuario Escluido com Sucesso");
                Login l = new Login();
                this.dispose();
                l.setVisible(true);
            } catch (Exception ex) {
                Logger.getLogger(UsuarioTela.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else System.out.println(u.getCodigo());
    }//GEN-LAST:event_jBExcluirActionPerformed

